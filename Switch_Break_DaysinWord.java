package AprilThird;

public class Switch_Break_DaysinWord {

	public static void main(String[] args) {
		int day=4;
		switch(day) {
		case 1:
			System.out.println("1-Mon");
			System.out.println("One");
			break;
			
		case 2:
			System.out.println("2-Tue");
			break;
			
		case 3:
			System.out.println("3-Wen");
			break;
			
		case 4:
			System.out.println("4-Thu");
			break;
			
		case 5:
			System.out.println("5-Fri");
			break;
			
		case 6:
			System.out.println("6-Sat");
			break;
			
		case 7:
			System.out.println("7-Sun");
			break;
		}
		
		

	}

}
