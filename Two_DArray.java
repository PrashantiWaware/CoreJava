package NineteenAprilPattern;

public class Two_DArray {

	public static void main(String[] args) {
		 int a [][]= {
				 {1,2,3,4,5},
				 {11,22,33,44,55},
				 {111,222,333,444,555}
		 };
		 for(int i=0;i<a.length;i++) {
			 for(int j=0;j<a[0].length;j++) {
				 System.out.print(a[i][j]+" ");
			 }
			 System.out.println();
		 }
				 
				
		

	
	}

}
